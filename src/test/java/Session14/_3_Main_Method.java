package Session14;

public class _3_Main_Method 
{
	//We can achieved overloading by create main more than one
	//We can write more than one main method inside same class

	public static void main(String[] args)
	{
		
		System.out.println("Inside main");
		main(10);//calling another main () method

	}
	
    public static void main(int a)
    {
		
		System.out.println("Inside main with a");

	}
	
	

}
